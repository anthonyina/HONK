#!/usr/bin/env node
const fs = require("fs");
const path = require("path");

const APP_ROOT = path.join(process.cwd(), "src", "app");
const OUTPUT_PATH = path.join(
  process.cwd(),
  "src",
  "generated",
  "valid-multipart-path-prefixes.ts",
);

const ROUTE_FILE_RE = /^(page|route)\.(js|jsx|ts|tsx)$/;

const shouldIgnoreSegment = (segment) =>
  segment.startsWith("(") || segment.startsWith("@");

const listRouteFiles = (dir) => {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const entryPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...listRouteFiles(entryPath));
    } else if (ROUTE_FILE_RE.test(entry.name)) {
      files.push(entryPath);
    }
  }

  return files;
};

const getRoutePrefix = (filePath) => {
  const dir = path.dirname(filePath);
  const relativeDir = path.relative(APP_ROOT, dir);
  if (!relativeDir) {
    return null;
  }

  const segments = relativeDir
    .split(path.sep)
    .filter(Boolean)
    .filter((segment) => !shouldIgnoreSegment(segment));

  if (!segments.length) {
    return null;
  }

  const isApiRoute = segments[0] === "api" || segments[0] === "_api";
  if (isApiRoute) {
    const staticSegments = [];
    for (const seg of segments) {
      if (seg.startsWith("[")) break;
      staticSegments.push(seg);
    }
    return `/${staticSegments.join("/")}`;
  }

  return `/${segments[0]}`;
};

const files = listRouteFiles(APP_ROOT);
const prefixes = new Set();

for (const filePath of files) {
  const prefix = getRoutePrefix(filePath);
  if (prefix) {
    prefixes.add(prefix);
  }
}

const sortedPrefixes = Array.from(prefixes).sort();
const fileContents = `// Auto-generated by scripts/generate-valid-multipart-path-prefixes.js
// Do not edit by hand.

export const VALID_MULTIPART_PATH_PREFIXES = ${JSON.stringify(
  sortedPrefixes,
  null,
  2,
)};
`;

fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
fs.writeFileSync(OUTPUT_PATH, fileContents, "utf8");

console.log(
  `Wrote ${sortedPrefixes.length} prefixes to ${path.relative(
    process.cwd(),
    OUTPUT_PATH,
  )}`,
);
